package org.xueyao_01;
/**
 * 学生类
 * 起名字我们要求做到见名知意
 * @author Yao Xue
 * @date Jul 3, 2017 1:43:34 PM
 */
public class Student {
    private String name;
    private int age;
    
    public void setName(String n) {
        name = n;
    }
    
    public String getName() {
        return name;
    }
    
    public void setAge(int a) {
        age = a;
    }
    
    public int getAge() {
        return age;
    }
}

