/*
a、定义一个byte类型的变量，并且为这个变量赋值，最后输出这个变量的值
	b、定义一个short类型的变量，并且为这个变量赋值，最后输出这个变量的值
	c、定义一个char类型的变量，并且为这个变量赋值，最后输出这个变量的值
	d、定义一个int类型的变量，并且为这个变量赋值，最后输出这个变量的值
	e、定义一个long类型的变量，并且为这个变量赋值，最后输出这个变量的值
	f、定义一个float类型的变量，并且为这个变量赋值，最后输出这个变量的值
	g、定义一个double类型的变量，并且为这个变量赋值，最后输出这个变量的值
	h、定义一个boolean类型的变量，并且为这个变量赋值，最后输出这个变量的值
*/
public class Test05 {
	public static void main(String[] args) {
		byte b = 5;
		System.out.println(b);

		short s = 500;
		System.out.println(s);

		char c = 'A';
		System.out.println(c);

		int i = 50000;
		System.out.println(i);

		long l = 500000L;
		System.out.println(l);

		float f = 55.555F;
		System.out.println(f);

		double d = 66.666;
		System.out.println(d);

		boolean bl = false;
		System.out.println(bl);
	}
}